# Tabela Verdade - AND
print(True and True)
print(True and False)
print(False and True)
print(False and False)

# Tabela Verdade - OR
print(True or True)
print(True or False)
print(False or True)
print(False or False)

# Tabela Verdade - XOR
print(True != True)
print(True != False)
print(False != True)
print(False != False)

# Operador de Negação - Unário
print(not True)
print(not False)

# Bit a Bit
print(True & True)
print(False | True)
print(True ^ False)
